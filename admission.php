          <dt>Admission Criteria 1:</dt>
          <dd>Persons at risk of severe disease- includes<br />• 1(a): People above the age of 60<br />
            • 1(b): Persons with Co-morbidities or associated medical conditions (HT, DM, IHD,
            cardiomyopathies, post-transplant patient, patients with malignancy, Chronic kidney disease,
            obesity, chronic liver disease, HIV infection, Chronic neurologic condition, COPD, etc.)<br />
            • 1(c): Patients on medications such as immunosuppressive therapy, patients on steroids</dd>
          <dt>Admission Criteria 2:</dt>
          <dd>Any patient with persistent fever more than 101 F for more than three days</dd>
          <dt>Admission Criteria 3:</dt>
          <dd>Any patient with oxygen saturation (SPO2) less than 94 % on room air, irrespective of age</dd>
          <dt>Admission Criteria 4:</dt>
          <dd>Any patient with significant breathing discomfort and increased respiratory rate irrespective of
            age</dd>
          <dt>Admission Criteria 5:</dt>
          <dd>Any patient with features suggestive of non-lung complications e.g. drowsiness, chest pain etc.
            irrespective of age</dd>
        </dl>

      </div>

      <div class="row mt-3 p-3">
